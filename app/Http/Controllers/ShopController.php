    public function collectionView(){
        $urlPath =  Request::url();

        $urlArray = explode("/" ,$urlPath);
        $category = $urlArray[3];
        $collection = $urlArray[5];

        $subCollections = Collections::all();

        return view('collection')->with([
            'category' => $category,
            'collection' => $collection,
            'subCollections' => $subCollections
            ]);
    }


